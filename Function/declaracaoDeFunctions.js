
//Modos de declarar funções

// 1- function declaration
function soma(a, b) {
    return a + b
}
console.log(soma(3, 2))
console.log(" ")
//===============================\\

// 2- function expression
const sub = function (a, b) {
    console.log(a - b)
}
sub(5, 2)
console.log(" ")

//===============================\\

// 3- named function expression

const mult1 = function mult(a, b) {
    return a * b
}

console.log(mult1(5, 2))





